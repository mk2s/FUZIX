/*
 *	Generate the syscall functions
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include "syscall_name.h"

static char namebuf[128];

static void write_call(int n)
{
  FILE *fp;
  snprintf(namebuf, 128, "fuzixmsp430x/syscall_%s.s", syscall_name[n]);
  fp = fopen(namebuf, "w");
  if (fp == NULL) {
    perror(namebuf);
    exit(1);
  }
  fprintf(fp, "\t.text\n\n"
	      "\t.globl _syscall\n"
	      "\t.globl %1$s\n\n"
	      "%1$s:\n", syscall_name[n]);

  if (syscall_args[n] == VARARGS)
  {
	/* Varargs syscalls have the first argument in r12 and the others on
	 * the stack. We support up to four parameters. */
	fprintf(fp, "\tmov 2(sp), r13\n"
	            "\tmov 4(sp), r14\n"
				"\tmov 6(sp), r15\n");
  }

  /* On entry, the four parameters are in r12-r15. The syscall number is on
   * the stack. */

  fprintf(fp, "\tpush #%d\n"
              "\tpush #_syscall_return\n"
			  "\tbr &(_start-2)\n",
			  n);
  fclose(fp);
}

static void write_call_table(void)
{
  int i;
  for (i = 0; i < NR_SYSCALL; i++)
    write_call(i);
}

static void write_makefile(void)
{
  int i;
  FILE *fp = fopen("fuzixmsp430x/Makefile", "w");
  if (fp == NULL) {
    perror("Makefile");
    exit(1);
  }
  fprintf(fp, "# Autogenerated by tools/syscall_mps430x\n");
  fprintf(fp, "CROSS_AS=msp430-elf-gcc\nCROSS_LD=msp430-elf-ld\nCROSS_AR=msp430-elf-ar\n");
  fprintf(fp, "ASOPTS=-g -c\n\n");
  fprintf(fp, "ASYS=syscall.s\n");
  fprintf(fp, "ASRCS = syscall_%s.s\n", syscall_name[0]);
  for (i = 1; i < NR_SYSCALL; i++)
    fprintf(fp, "ASRCS += syscall_%s.s\n", syscall_name[i]);
  fprintf(fp, "\n\nASRCALL = $(ASRCS) $(ASYS)\n");
  fprintf(fp, "\nAOBJS = $(ASRCALL:.s=.o)\n\n");
  fprintf(fp, "syslib.lib: $(AOBJS)\n");
  fprintf(fp, "\techo $(AOBJS) >syslib.l\n");
  fprintf(fp, "\t$(CROSS_AR) rc syslib.lib $(AOBJS)\n\n");
  fprintf(fp, "$(AOBJS): %%.o: %%.s\n");
  fprintf(fp, "\t$(CROSS_AS) $(ASOPTS) -o $*.o $<\n\n");
  fprintf(fp, "clean:\n");
  fprintf(fp, "\trm -f $(AOBJS) $(ASRCS) syslib.lib syslib.l *~\n\n");
  fclose(fp);
}

int main(int argc, char *argv[])
{
  write_makefile();
  write_call_table();
  exit(0);
}
